#Crie um programa em Python que solicite o valor em horas para o usuário, calcule e
#mostre o valor em minutos, sabendo que 1 hora tem 60 minutos.







#Solicitando o valor em horas
horas = float(input("Digite o valor em horas: "))

#Calculando o valor em minutos
minutos = horas * 60

#Mostrando o resultado
print("O valor em minutos é: ", minutos)
